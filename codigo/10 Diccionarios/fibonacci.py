# Diccionario para los n√∫meros de fibonacci
anteriores = {0:1, 1:1 }

def fibonacci(n):
    if n in anteriores:
        return anteriores[n]
    else:
        nuevo = fibonacci(n-1)+fibonacci(n-2)
        anteriores[n] = nuevo
    return nuevo


for j in range(30):
    print("fibonacci(",j,")=\t", fibonacci(j))






































































































































































                           




